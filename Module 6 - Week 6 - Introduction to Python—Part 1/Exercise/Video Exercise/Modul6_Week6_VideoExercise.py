#Variables, Data Types, and Operators in Python
var1 = 1
print(type(var1))   # this will output int
var1= "1"
print(type(var1))   # this will output str 
var1= 1.0
print(type(var1))   # this will output float
var1= True
print(type(var1))   # this will output bool

